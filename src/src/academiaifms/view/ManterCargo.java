    private void limparCampos() {
        txtAreaDescricao.setText("");
        txtNome.setText("");
        txtSalario.setText("");
    }

    private void populaTabelaCargo() {
        DefaultTableModel model = (DefaultTableModel) jtableCargos.getModel();
        model.setRowCount(0);
        List<CargoMapeamento> listaDeCargos = this.cargoRepo.buscarTodos();
        for (int i = 0; i < listaDeCargos.size(); i++) {
            model.addRow(new Object[]{
                listaDeCargos.get(i).getIdCargo(),
                listaDeCargos.get(i).getNome(),
                listaDeCargos.get(i).getSalario(),
                listaDeCargos.get(i).getDescricao()
            });
        }
        jtableCargos.setModel(model);
    }

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        CargoMapeamento cargo = new CargoMapeamento();

        cargo.setNome(txtNome.getText());
        cargo.setDescricao(txtAreaDescricao.getText());
        try {
            double valor3 = Double.parseDouble(txtSalario.getText());
            cargo.setSalario(valor3);
            this.cargoRepo.salvar(cargo);
            populaTabelaCargo();

            limparCampos();
            JOptionPane.showMessageDialog(null, "Salvo com Sucesso!!" );
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Campo valor inválido:" + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

